#Program for illustrating concatenation

#list concatenation

li1 = [1,2,3]
li2 = [4,5,6]
li3 = li1 + li2
print("After concatenation: ", li3)

#tuple concatenation

tup1 = (1,2,3)
tup2 = (4,5,6)
tup3 = tup1 + tup2
print("After concatenation: ", tup3)

#String concatenation

str1 = "Hello"
str2 = "World"
str3 = str1 + str2
print("After concatenation: ", str3)

#Program for list

#append

li = [1,2,3,4,5]
print("Before appending:list = ", li)
li.append(6)
print("After appending:list = ", li)

#clear

li = [1,2,3,4]
print("Before clearing:list = ", li)
li.clear()
print("After clearing:list = ", li)

#pop

li = [1,2,3,4]
print("Before deleting:list = ", li)
li.pop(1)
print("After deleting:list = ", li)
